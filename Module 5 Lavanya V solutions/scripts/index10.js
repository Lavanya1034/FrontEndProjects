//input as number and performing unary operation

//get the input number
let inp = parseInt(window.prompt("Enter the number"));
console.log(inp);

//increementing the value by 1
let inpAdd = ++inp;
console.log(`Result after adding is ${inpAdd}`);

//decreementing the value by 1

let inpSub = --inp;
console.log(`Result after subtraction is ${inpSub}`);

//Boolean value 
let bool = !inp;
console.log(`Boolean value is ${bool}`);



